function getRandom(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}

function generate() {
  const results = document.getElementById("results");
  results.innerHTML = "";

  for (let i = 0; i < 3; i++) {
    const comboDiv = document.createElement("div");
    comboDiv.className = "combo";
    comboDiv.innerHTML = `
      <div class="card vid"><p>Вид</p><p>${getRandom(vid)}</p></div>
      <div class="card obekt"><p>Объект</p><p>${getRandom(obekt)}</p></div>
      <div class="card kachestvo"><p>Качество</p><p>${getRandom(kachestvo)}</p></div>
    `;
    results.appendChild(comboDiv);
  }
}
